-- (선택) 기존 옵션 라벨에서 잉글랜드/스코틀랜드 깨진 국기 제거
-- =============================================================
-- 이 패치 이전에 만든 카테고리의 팀 옵션 라벨에는 서브디비전 국기(🏴+태그)가
-- 그대로 박혀 있어 일부 화면(라이브/정산 칩)에서 깨져 보일 수 있다.
-- 아래로 해당 옵션 라벨을 팀 이름만 남기도록 정리. (새로 만드는 옵션은 이미 정상)
-- =============================================================

update options o
set label = t.name
from teams t
where o.team_id = t.id
  and t.fifa_code in ('ENG', 'SCO', 'WAL');
